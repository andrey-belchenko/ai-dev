CREATE TABLE ASUSE.RR_PRESET_LOG (
    KOD_PRESET    NUMBER(10)      NOT NULL,
    KOD_PSUBJ     NUMBER(10),
    NAME          VARCHAR2(200),
    U_M           VARCHAR2(20),
    D_M           DATE,
    PR_PROTECTED  NUMBER(1),
    TMP           NUMBER(1),
    TITLE         VARCHAR2(4000),
    TYPE_APP      NUMBER(10),
    PR_DEFAULT    NUMBER(1),
    D_D           DATE
);

CREATE PUBLIC SYNONYM RR_PRESET_LOG FOR ASUSE.RR_PRESET_LOG;

GRANT SELECT ON ASUSE.RR_PRESET_LOG TO INFOENERGO;
GRANT SELECT ON ASUSE.RR_PRESET_LOG TO PUBLIC;
GRANT SELECT ON ASUSE.RR_PRESET_LOG TO READ_PUBL;
GRANT DELETE, INSERT, SELECT, UPDATE ON ASUSE.RR_PRESET_LOG TO REPADMIN;
GRANT DELETE, INSERT, SELECT, UPDATE ON ASUSE.RR_PRESET_LOG TO WRITE_S;

CREATE TABLE ASUSE.RR_PRESETOPTION_LOG (
    KOD_POPTION   NUMBER(10),
    KOD_PRESET    NUMBER(10),
    VAL_N         NUMBER(20,2),
    VAL_S         VARCHAR2(250),
    VAL_D         DATE,
    ORDNUM        NUMBER(5)       DEFAULT 1,
    DT            NUMBER(1),
    OPER          VARCHAR2(10),
    VAL_N1        NUMBER,
    VAL_S1        VARCHAR2(50),
    VAL_D1        DATE,
    OPERNOT       NUMBER(1),
    OPEROR        NUMBER(1),
    VAL_SL        VARCHAR2(254),
    NUM           NUMBER(10),
    D_D           DATE
);

CREATE PUBLIC SYNONYM RR_PRESETOPTION_LOG FOR ASUSE.RR_PRESETOPTION_LOG;

GRANT SELECT ON ASUSE.RR_PRESETOPTION_LOG TO INFOENERGO;
GRANT SELECT ON ASUSE.RR_PRESETOPTION_LOG TO PUBLIC;
GRANT SELECT ON ASUSE.RR_PRESETOPTION_LOG TO READ_PUBL;
GRANT DELETE, INSERT, SELECT, UPDATE ON ASUSE.RR_PRESETOPTION_LOG TO REPADMIN;

CREATE OR REPLACE TRIGGER ASUSE.RT_PRESET_LOG
AFTER INSERT OR UPDATE OR DELETE ON ASUSE.RR_PRESET
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO ASUSE.RR_PRESET_LOG (
            KOD_PRESET, KOD_PSUBJ, NAME, U_M, D_M,
            PR_PROTECTED, TMP, TITLE, TYPE_APP, PR_DEFAULT
        ) VALUES (
            :NEW.KOD_PRESET, :NEW.KOD_PSUBJ, :NEW.NAME, :NEW.U_M, :NEW.D_M,
            :NEW.PR_PROTECTED, :NEW.TMP, :NEW.TITLE, :NEW.TYPE_APP, :NEW.PR_DEFAULT
        );
    ELSIF UPDATING THEN
        UPDATE ASUSE.RR_PRESET_LOG SET
            KOD_PSUBJ    = :NEW.KOD_PSUBJ,
            NAME         = :NEW.NAME,
            U_M          = :NEW.U_M,
            D_M          = :NEW.D_M,
            PR_PROTECTED = :NEW.PR_PROTECTED,
            TMP          = :NEW.TMP,
            TITLE        = :NEW.TITLE,
            TYPE_APP     = :NEW.TYPE_APP,
            PR_DEFAULT   = :NEW.PR_DEFAULT
        WHERE KOD_PRESET = :NEW.KOD_PRESET;
    ELSIF DELETING THEN
        UPDATE ASUSE.RR_PRESET_LOG SET
            D_D = SYSDATE
        WHERE KOD_PRESET = :OLD.KOD_PRESET;
    END IF;
END;
