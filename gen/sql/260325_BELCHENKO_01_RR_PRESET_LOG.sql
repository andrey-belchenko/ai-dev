CREATE TABLE ASUSE.RR_PRESET_LOG (
    KOD_PRESET    NUMBER(10)      NOT NULL,
    KOD_PSUBJ     NUMBER(10),
    NAME          VARCHAR2(200),
    U_M           VARCHAR2(20),
